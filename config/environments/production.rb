    provider: 'AWS',
    aws_access_key_id: ENV['AWS_ACCESS_KEY'],
    aws_secret_access_key: ENV['AWS_SECRET_KEY']
  }

  config.fog_directory = "waywt-#{Rails.env}"
  end
end
